let x = Math.round(Math.random() * 100 + 1);
let y = Math.round(Math.random() * 50 + 1);

console.log(`Value of x is ${x}`);
console.log(`Value of y is ${y}`);
console.log(`${x} + ${y} = ${x + y}`);
console.log(`${x} - ${y} = ${x - y}`);
console.log(`${x} * ${y} = ${x * y}`);
console.log(`${x} / ${y} = ${x / y}`);
console.log(`${x} % ${y} = ${x % y}`);